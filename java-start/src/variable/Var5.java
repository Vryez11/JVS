package variable;

public class Var5 {
    public static void main(String[] args) {
        //변수 선언과 초기화 다르게
        int a;
        a = 1;
        System.out.println(a);

        int b = 2; //변수 선언과 초기화를 한번에!
        System.out.println(b);

        //여러 변수를 한번에 선언 및 초기화
        int c = 3, d = 4;

        System.out.println(c);
        System.out.println(d);
    }
}
